// Declaro variables (en cm)
const lado1 = 10;
const lado2 = 5;

// Cálculo del área
const area = lado1 * lado2;

// Cálculo del perímetro
const perimetro = 2 * (lado1 + lado2);

// Mostrar resultados en OUTPUT
process.stdout.write(`El área del rectángulo es: ${area} cm²\n`);
process.stdout.write(`El perímetro del rectángulo es: ${perimetro} cm\n`);